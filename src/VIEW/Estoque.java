    public void listData(){
        EstoqueController EstoqueController = new EstoqueController();
        DefaultTableModel TabelaProdutos = (DefaultTableModel)jTable1.getModel();
        
        while(TabelaProdutos.getRowCount() > 0){
            TabelaProdutos.removeRow(0);
        }
        
        EstoqueController.preencherDados(TabelaProdutos);
    }
    
    public void zerarText(){
        jTextField2.setText("");
        jTextField1.setText("");
    }
    
