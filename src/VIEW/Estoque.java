    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTable1.getSelectedRow()!= -1){
            EstoqueController EstoqueController = new EstoqueController();
            
            EstoqueController.excluirEstoque(Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString()));
            zerarText();
            listData();
        }else{
            JOptionPane.showMessageDialog(null, "Por Favor Selecione um Produto!", "Alerta", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

