    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (jTable1.getSelectedRow()!= -1){
            Estoque = jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString();
            ID = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            jTextField1.setText(Estoque);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTable1.getSelectedRow()!= -1){
            EstoqueController EstoqueController = new EstoqueController();

            try {
                EstoqueResult = Integer.parseInt(Estoque) + Integer.parseInt(jTextField2.getText());
                EstoqueController.alterarEstoque(ID, EstoqueResult);
                zerarText();
                listData();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Campo vazio ou dados incorretos!", "Alerta", JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Por Favor Selecione um Produto!", "Alerta", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

